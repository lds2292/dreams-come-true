/**
 * 검색어에 동의어 전처리를 적용한다.
 * @param {string} query 원본 검색어
 * @returns {string} 전처리된 검색어
 */
function applySymonyms(query) {
  let result = query;
  for (const { from, to } of SYNONYM_MAP) {
    if (result.includes(from)) {
      result = result.replaceAll(from, to);
    }
  }
  return result;
}

